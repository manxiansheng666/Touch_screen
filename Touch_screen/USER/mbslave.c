//16位CRC校验函数,查表法
uint16_t crc16(uint8_t *puchMsg,uint16_t usDataLen) 	
{ 											
	uint8_t CRCHi = 0xFF ; 				
	uint8_t CRCLo = 0xFF ; 				
	uint16_t uIndex ; 							
	while (usDataLen--) 					
	{
		uIndex = CRCHi ^ *puchMsg++ ; 	
		CRCHi = CRCLo ^ auchCRCHi[uIndex] ;
		CRCLo = auchCRCLo[uIndex] ;
	}
	return (((uint16_t)(CRCLo) << 8) | CRCHi) ;
}

void modbus_process(uint8_t *Rx0_buf,uint8_t len,uint8_t station_addr)
{
	if(len < 8)			//收到的数据少于8个，不是完整的帧
		return;
	if(crc16(Rx0_buf,len) != 0x00)		//crc校验不通过
		return;
	if(station_addr != Rx0_buf[0])	//不是本站的数据，不处理
		return;
	uint8_t addrtemp;
	uint8_t lenthtemp;
	uint8_t Tx0_nums;
	uint16_t crc_end;
	uint8_t tmp_bits;
	uint8_t tmp_bytes;
	uint8_t j = 0;
	uint8_t Function = Rx0_buf[1];	//获取功能码
	uint8_t Rx0_addr=((uint16_t)Rx0_buf[2]<<8)+Rx0_buf[3];	
	uint8_t Rx0_lenth=((uint16_t)Rx0_buf[4]<<8)+Rx0_buf[5];	
	switch(Function)
	{
		case 0x01:
			addrtemp=Rx0_addr/8;
			lenthtemp=Rx0_lenth/8;//位 字节数
			Tx0_nums=lenthtemp+5;
			Tx0_buf[0]= Rx0_buf[0];
            Tx0_buf[1]= Rx0_buf[1];
            Tx0_buf[2]= lenthtemp;
			j = 3;
			for(uint8_t i=0;i<lenthtemp;i++)
			{
				Tx0_buf[j]=OUTPUT[addrtemp++];
				j++;
			}	  
			crc_end=crc16(Tx0_buf,j);				//校验
			Tx0_buf[j++]=crc_end%256;				//校验低位
			Tx0_buf[j++]=crc_end/256;				//校验高位
			uart2_send_by485(Tx0_buf,Tx0_nums);
			break;
			
		case 0x02:
			addrtemp=Rx0_addr/8;
			lenthtemp=Rx0_lenth/8;//位 字节数
			Tx0_nums=lenthtemp+5;
			Tx0_buf[0]= Rx0_buf[0];
            Tx0_buf[1]= Rx0_buf[1];
            Tx0_buf[2]= lenthtemp;
			j=3;
			for(uint8_t i=0;i<lenthtemp;i++)
			{
				Tx0_buf[j]=INPUT[addrtemp+i];
				j++;
			}	  
			crc_end=crc16(Tx0_buf,j);				//校验
			Tx0_buf[j++]=crc_end%256;				//校验低位
			Tx0_buf[j++]=crc_end/256;				//校验高位
			uart2_send_by485(Tx0_buf,Tx0_nums);
			break;
			
		case 0x03:
			lenthtemp=Rx0_lenth*2;
		    Tx0_nums=lenthtemp+5;
			Tx0_buf[0]= Rx0_buf[0];
            Tx0_buf[1]= Rx0_buf[1];
            Tx0_buf[2]= lenthtemp;
            j=3;
			for(uint8_t i = 0;i < Rx0_lenth;i++)	//发送相应字节	
			{							//字高位为0
				Tx0_buf[j]=OUTREG[i+Rx0_addr]/256;
				j++;
				Tx0_buf[j]=OUTREG[i+Rx0_addr]%256;			//字低位为写入字
				j++;
			}   
			crc_end=crc16(Tx0_buf,lenthtemp+3);				//校验
			Tx0_buf[lenthtemp+3]=crc_end%256;				//校验低位
			Tx0_buf[lenthtemp+4]=crc_end/256;				//校验高位
			uart2_send_by485(Tx0_buf,Tx0_nums);
			break;
			
		case 0x04:
			lenthtemp=Rx0_lenth*2;
		    Tx0_nums=lenthtemp+5;
			Tx0_buf[0]= Rx0_buf[0];
            Tx0_buf[1]= Rx0_buf[1];
            Tx0_buf[2]= lenthtemp;
            j=3;
			for(uint8_t i = 0;i < Rx0_lenth;i++)	//发送相应字节	
			{							//字高位为0
				Tx0_buf[j]=INREG[i+Rx0_addr]/256;
				j++;
				Tx0_buf[j]=INREG[i+Rx0_addr]%256;			//字低位为写入字
				j++;
			}   
			crc_end=crc16(Tx0_buf,lenthtemp+3);				//校验
			Tx0_buf[lenthtemp+3]=crc_end%256;				//校验低位
			Tx0_buf[lenthtemp+4]=crc_end/256;				//校验高位
			uart2_send_by485(Tx0_buf,Tx0_nums);
			break;
		
		case 0x05:
			tmp_bytes=Rx0_addr/8;         //最多到255位，只用了低8位
            tmp_bits=Rx0_addr%8;
			if(Rx0_buf[4]==0xff)
			{
				OUTPUT[tmp_bytes]|=(1<<tmp_bits);//flag_switchtmpr[tmp_bytes]|=(1<<tmp_bits);
			}
			else if(Rx0_buf[4]==0x00)
			{
				OUTPUT[tmp_bytes]&=~(1<<tmp_bits);//flag_switchtmpr[tmp_bytes]&=~(1<<tmp_bits);
			}  
			uart2_send_by485(Rx0_buf,len);	//返回收到的数据
			break;
			
		case 0x06:
			OUTREG[Rx0_addr]=(uint16_t)Rx0_buf[4]<<8 | Rx0_buf[5];
			uart2_send_by485(Rx0_buf,len);	//返回收到的数据
			break;
		
		case 0x10:	
			j = 0;
			Tx0_nums=8;
			for(uint8_t i = 0;i < Rx0_lenth;i++)	//发送相应字节	
			{
				OUTREG[i + Rx0_addr]=(uint16_t)Rx0_buf[7+j]<<8 | Rx0_buf[8+j];
   		      	j+=2;
			}
			for(uint8_t i = 0;i < 6;i++)
			{
				Tx0_buf[i] = Rx0_buf[i];
			}
			crc_end=crc16(Tx0_buf,6);				//校验
			Tx0_buf[6]=crc_end%256;				   //校验低位
			Tx0_buf[7]=crc_end/256;				   //校验高位
			uart2_send_by485(Tx0_buf,Tx0_nums);	//返回收到的数据
			break;
		default:
			break;
	}	
}
